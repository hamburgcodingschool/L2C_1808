// arithmetic operators

// +  -  *  /  %

var n1 = 7;
var n2 = 5;

var sum = n1 + n2;
var sub = n1 - n2;
var mult = n1 * n2;
var divi = n1 / n2;
var mod = n1 % n2;

// Concatenation

console.log("The sum is " + sum);
console.log("The subtraction is " + sub);
console.log("The ... is " + mult);
console.log("The ... is " + divi);
console.log("The ... is " + mod);

//  7 + 5 = 12
// console.log("7 + 5 = 12");

console.log(n1 + " + " + n2 + " = " + sum);
console.log(n1 + " - " + n2 + " = " + sub);
console.log(n1 + " x " + n2 + " = " + mult);
console.log(n1 + " / " + n2 + " = " + divi);
console.log(n1 + " mod " + n2 + " = " + mod);
